import * as fs from "fs";

import { ethers } from "hardhat";

import { DEPLOYED_NFT_VOTING_ESCROW } from "./config";

async function main() {
  const [deployer] = await ethers.getSigners();
  console.log(`Using wallet address: ${deployer.address}.`);

  const { provider } = ethers;
  const estimateGasPrice = await provider.getGasPrice();
  const gasPrice = estimateGasPrice.mul(5).div(2);
  console.log(`Gas Price: ${ethers.utils.formatUnits(gasPrice, `gwei`)} gwei`);

  const oldMAHAXInterface = new ethers.utils.Interface(
    JSON.stringify(OLD_MAHAX_ABI)
  );
  const oldMAHAX = new ethers.Contract(
    DEPLOYED_NFT_VOTING_ESCROW,
    oldMAHAXInterface,
    deployer
  );

  const lastNFTId = 600;
  const snapshotData: { [tokenId: number]: any } = {};
  for (let i = 590; i <= lastNFTId; i++) {
    console.log(`Fetching lock details for ${i}`);
    const locked = await oldMAHAX.locked(i);
    if (locked.amount.eq(0)) continue;
    snapshotData[i] = locked;
  }

  fs.writeFileSync(
    `./output/userLockSnapshot.json`,
    JSON.stringify(snapshotData)
  );
}

